package net.epicyclic.common;

public class PortletIntegrationRuntimeException extends RuntimeException {

   private static final long serialVersionUID = -7330888524971004289L;

   public PortletIntegrationRuntimeException() {
   }

   public PortletIntegrationRuntimeException(String arg0) {
      super(arg0);
   }

   public PortletIntegrationRuntimeException(Throwable arg0) {
      super(arg0);
   }

   public PortletIntegrationRuntimeException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

}
