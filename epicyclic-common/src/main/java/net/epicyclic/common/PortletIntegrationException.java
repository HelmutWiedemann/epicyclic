package net.epicyclic.common;

public class PortletIntegrationException extends Exception {

   /**
    * 
    */
   private static final long serialVersionUID = -8123843527680593232L;

   public PortletIntegrationException() {
   }

   public PortletIntegrationException(String arg0) {
      super(arg0);
   }

   public PortletIntegrationException(Throwable arg0) {
      super(arg0);
   }

   public PortletIntegrationException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

}
